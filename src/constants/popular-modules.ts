import { MonoRandom } from "monorandom";
import { shuffled } from "../utils";

export function getPopularModules(rnd?: MonoRandom): string[] {
  const map = new Map<number, string[]>();
  for (const [id, prior] of popularModules) map.set(prior, [...(map.get(prior) || []), id]);
  for (const [prior, arr] of map.entries()) {
    if (arr.length < 2) continue;
    map.set(prior, shuffled(arr, rnd));
  }
  const res: string[] = [];
  for (const prior of [...map.keys()].sort((a, b) => b - a)) {
    const arr = map.get(prior)!;
    res.push(...arr);
  }
  return res;
}

